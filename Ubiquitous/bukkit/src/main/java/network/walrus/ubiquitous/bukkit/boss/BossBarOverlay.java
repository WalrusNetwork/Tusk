package network.walrus.ubiquitous.bukkit.boss;

/**
 * Possible overlays that a {@link BossBar} can have.
 *
 * @author kashike
 */
public enum BossBarOverlay {
  /** A progress bar overlay. */
  PROGRESS,
  /** An overlay with 6 segments. */
  NOTCHED_6,
  /** An overlay with 10 segments. */
  NOTCHED_10,
  /** An overlay with 12 segments. */
  NOTCHED_12,
  /** An overlay with 20 segments. */
  NOTCHED_20
}
